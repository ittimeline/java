package net.ittimeline.java.foundational.control.forstatement;

/**
 * for循环输出5个HelloWorld
 *
 * @author tony 18601767221@163.com
 * @version 2021/3/16 21:57
 * @since JDK11
 */
public class ForHelloWorld {


    public static void main(String[] args) {
        // 需求  往控制台打印输出5个HelloWorld

        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("楚河************汉界****************");


        /*

        1. 初始化循环条件语句
        2. 循环条件判断语句
        3. 循环体语句
        4. 循环迭代语句

         */
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World");
        }
    }
}
