package net.ittimeline.java.foundational.control.forstatement;

/**
 * 往控制台打印输出100-1
 *
 * @author tony 18601767221@163.com
 * @version 2021/3/16 22:04
 * @since JDK11
 */
public class For100To1 {

    public static void main(String[] args) {

        // 往控制台打印输出100-1

        for (int i =100; i >=1 ; i--) {
            System.out.print(i+" ");
        }
    }
}
