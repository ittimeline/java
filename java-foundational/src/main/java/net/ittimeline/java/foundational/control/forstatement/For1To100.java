package net.ittimeline.java.foundational.control.forstatement;

/**
 * 打印输出1-100的整数
 *
 * @author tony 18601767221@163.com
 * @version 2021/3/16 22:02
 * @since JDK11
 */
public class For1To100 {


    public static void main(String[] args) {

        // 需求 往控制台打印输出1-100的整数

        for (int i = 1; i <=100 ; i++) {
            System.out.print(i +" ");
        }
    }
}
