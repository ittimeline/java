public class Keywords {
    public static void main(String[] args) {
        //将特殊值true赋值给变量flag
        boolean flag = true;
        //字符串拼接打印输出变量flag存储的值
        System.out.println("1.flag = " + flag);

        //将特殊值false赋值给变量flag
        flag = false;
        //字符串拼接打印输出变量flag存储的值
        System.out.println("2.flag = " + flag);

        //将特殊值null赋值给变量str
        String str = null;
        //字符串拼接打印输出变量str存储的值
        System.out.println("3.str = " + str);
    }
}
